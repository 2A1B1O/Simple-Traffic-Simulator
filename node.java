
public class node {
	private int numberOfCars;
	private double x;
	private double y;
	
	public node(int ncars)
	{
		numberOfCars=ncars;
	}
public void setNumberOfCars(int newValue)
{
	numberOfCars=newValue;
}
public int getNumberOfCars()
{
	return numberOfCars;
}
public void setX(double newX)
{
	x=newX;
	}
public double getX()
{
	return x;
	}
public void setY(double newY)
{
	y=newY;
}
public double getY()
{
	return y;
	}
}
